    <h1 class="title">About me</h1>
    <div class="articol">
        <p>
            <img class="eu" src="../images/eu3.jpg" alt="Adrian Manea">
            My intellectual interests and preoccupations have been, for as long as I can remember, centered around theoretical subjects. I am usually clumsy in practical things, but the theoretical sides of many sciences have exterted a powerful attraction to me.
        </p>

        <p>
            Thus, I went to study for my Bachelor's degree at <a href="http://fizica.unibuc.ro/Fizica/Main.php" target="_blank">the Faculty of Physics</a> of the University of Bucharest, intending to specialize in <b>theoretical physics</b>, since it seems to me the best way of interpreting mathematics. I like to say that <i>physics is a mathematics that is censored by reality</i> and I was really fascinated how abstract quantities, variables, functions, spaces find a correspondence in reality.
        </p>

        <p>
            Unfortunately, I felt that the theoretical side is not developed as I wanted, so after graduating, I followed  Master's program in <b>algebra</b> at  <a href="http://fmi.unibuc.ro/ro/" target="_blank">the Faculty of Mathematics and Computer Science (FMI)</a>, also belonging to the University of Bucharest. I always liked algebra particularly, because it seems to me that it organizes reasoning and arguments in an exemplary way, (almost) axiomatically, in many cases. It does not leave much room for intuition or physical interpretations, at least when compared to Mathematical Analysis or Geometry, for example, but in its abstractions I have always found a world to resonate in me.
        </p>

        <p>
            I felt afterwards that many perspectives opened which did not necessarily lead (back) to physics and which I embraced. After finishing the Master's studies, I went to pursue a doctorate in a domain of <b>homological algebra</b>, at the same faculty, coordinated by <a href="http://fmi.unibuc.ro/ro/scoala_doctorala_mate/dragos_stefan" target="_blank">Prof. Dragoș Ștefan</a>. Throughout my entire transition from physics to mathematics, I was supported with great warmth, particularly by <a href="http://fmi.unibuc.ro/ro/scoala_doctorala_mate/sorin_dascalescu" target="_blank">Prof. Sorin Dăscălescu</a>.
        </p>

        <p>
            In parallel, the idea of interpreting and commenting abstract theories started drawing an increasing attraction to me, more so because I love communicating my ideas and the teaching component of my career means very much to me. Thus, I went to hear courses of <b>history and philosophy of science</b> at the <a href="http://filosofie.unibuc.ro" target="_blank">Faculty of Philosophy</a> in Bucharest, where I had the honor of talking to and learning a lot from <a href="http://filosofie.unibuc.ro/daniela-rodica-jalobeanu/" target="_blank">Prof. Dana Jalobeanu</a>, <a href="http://filosofie.unibuc.ro/sorin_costreie/" target="_blank">Prof. Sorin Costreie</a> and Dr. Alexandru Dragomir, with whom I also had the pleasure of teaching a one semester course on the philosophy of mathematics. Thus, my interest for the history and philosophy of science was born, especially in the area of mathematics and modern algebra, in particular (starting from the 19th century).
        </p>

        <p>
            My current philosophical interests also include <b>phenomenology (E. Husserl, F. Brentano, M. Heidegger), existentialism (A. Camus, J. P. Sartre, F. Nietzsche, F. Dostoievski) and intuitionism (A. Heyting, L. E. J. Brouwer).</b>
        </p>

        <p>
            Currently, I am a teaching assistant at the <a href="http://dep2.mathem.pub.ro/index_en.html" target="_blank">Department of Mathematical Methods and Models</a> of the Faculty of Applied Sciences at the Politehnica University of Bucharest and I teach <a href="4-didactica.php" target="_parent">seminars</a> of algebra, calculus and other mathematics for undergraduates.
        </p>

        <p>
            My research interests currently include <b>categorical logic, topos theory, homotopy theory, intuitionistic logic, type theory and functional programming.</b>
        </p>
        

        
        <p>
            Above all these or, better said, as a background, my passion for <b>literature</b> kept growing. When I feel the need of a motivation for this, I think of the fact that I should not allow the existence of any thought, idea, feeling or sensation that I cannot express precisely and clearly in words. If I feel I don't have the words for something inside me, I blame this on the lack of readings. I really feel how my vocabulary is enriched and also how I develop feelings of empathy or which allow me to make comparisons or analogies with literary situations or characters. And all of these lead, invariably, to the improvement of my clarity and precision, which was the initial aim.
        </p>

        <p>
            Therefore, on this website, you will find pieces that are more or less rugged, shorter or longer, scientific or from my soul, emerging from the mix of all the above, which is me.
        </p>

