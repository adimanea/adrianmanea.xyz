    </p>

    <p>
      Din păcate, însă, am simțit că latura teoretică nu este dezvoltată așa cum mi-aș fi dorit, așa că am urmat apoi un master în <b>algebră</b> la <a href="http://fmi.unibuc.ro/ro/" target="_blank">Facultatea de Matematică și Informatică (FMI)</a>, tot a Universității din București. Algebra mi-a plăcut mereu în mod deosebit, pentru că mi se pare că organizează un raționament și o demonstrație în mod exemplar, (aproape) axiomatic, în multe cazuri. Nu lasă foarte mult loc intuiției sau interpretărilor fizice, precum o fac analiza ori geometria, de exemplu, dar în abstracțiunile sale am găsit mereu o lume cu care rezonez.
    </p>

    <p>
