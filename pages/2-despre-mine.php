    <p>
      <img class="eu" src="../images/eu3.jpg" alt="Adrian Manea">
      Interesele și preocupările mele intelectuale au fost de cînd îmi amintesc centrate în jurul subiectelor teoretice. Sînt, de obicei, neîndemînatic în lucruri practice, dar părțile teoretice ale multor științe m-au atras.
    </p>

